// luas persegi
var luaspersegi = function (s) {
  return s * s
}
 
console.log(luaspersegi(3))

// luas segitiga
var luassegitiga = function(alas, tinggi) {   
   return alas * tinggi * 1/2 
}
console.log(luassegitiga(2, 4))

// luas lingkaran
var luaslingkaran = function(r) {   
   return r * r * 3.14 
}
console.log(luaslingkaran(10))